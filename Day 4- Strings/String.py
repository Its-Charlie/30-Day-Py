#Creating Strings:
first_name = "Siddhesh"
last_name = "Yadav"

print(first_name)
print(last_name)

#String Concatenation:
first_name = "Siddhesh"
last_name = "Yadav"

full_name = first_name + " " + last_name

print(full_name)

#String Repetition:
word = "Python "
print(word * 3)

#String Length:
text = "Artificial Intelligence"

print(len(text))

#Character Indexing:
language = "Python"

print(language[0])
print(language[1])
print(language[5])

print(language[-1])
print(language[-2])
print(language[-6])