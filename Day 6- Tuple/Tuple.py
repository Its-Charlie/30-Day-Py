#Creating a Tuple
fruits = ("Apple", "Banana", "Mango")
print(fruits)

#Tuple with Different Data Types
data = ("Siddhesh", 22, 75.5, True)
print(data)

#Accessing Tuple Elements
fruits = ("Apple", "Banana", "Mango")
print(fruits[0])
print(fruits[1])
print(fruits[2])

#Tuple Slicing
numbers = (10, 20, 30, 40, 50)

print(numbers[1:4])
print(numbers[:3])
print(numbers[2:])

#Tuple Length
fruits = ("Apple", "Banana", "Mango")

print(len(fruits))