#Creating a List
fruits = ["Apple", "Banana", "Mango"]
print(fruits)

#Accessing List Elements
fruits = ["Apple", "Banana", "Mango"]
print(fruits[0])
print(fruits[1])
print(fruits[2])

#Negative Indexing
fruits = ["Apple", "Banana", "Mango"]
print(fruits[-1])
print(fruits[-2])

#List Slicing
numbers = [10, 20, 30, 40, 50]
print(numbers[1:4])
print(numbers[:3])
print(numbers[2:])